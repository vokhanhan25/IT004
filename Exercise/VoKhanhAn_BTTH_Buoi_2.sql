	MAGV CHAR(4) CONSTRAINT GIAOVIEN_MAGV_PK PRIMARY KEY(MAGV),
	HOTEN VARCHAR(40),
	HOCVI VARCHAR (10),
	HOCHAM VARCHAR(10),
	NGSINH SMALLDATETIME,
	NGVL SMALLDATETIME,
	HESO NUMERIC(4,2),
	MUCLUONG MONEY,
	MAKHOA VARCHAR(4)
)

